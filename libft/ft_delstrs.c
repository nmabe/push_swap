#include "libft.h"

void    ft_delstrs(char ***str)
{
    int     i;

    i = 0;
    while (*str[0][i] != '\0')
    {
        ft_strdel(&(str[0][i]));
        i++;
    }
    free(str[0]);
    str[0] = NULL;
}
