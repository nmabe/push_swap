#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int counter1;

	counter1 = 0;
	if (!s || !f)
		return ;
	while (s[counter1] != '\0')
	{
		f(&s[counter1]);
		counter1++;
	}
}
